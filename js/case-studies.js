// Load case study data when page loads
$(document).ready(function() {
    const urlParams = new URLSearchParams(window.location.search);
    const caseId = urlParams.get('case');
    
    if (caseId && caseStudiesData[caseId]) {
        loadCaseStudyData(caseStudiesData[caseId]);
    } else {
        // Show all case studies if no specific case is selected
        showAllCaseStudies();
    }
});

function loadCaseStudyData(caseStudy) {
    // Update page title
    document.title = `${caseStudy.title} - Case Study`;
    
    // Update header
    $('#case-title').text(caseStudy.title);
    $('#case-header-image').attr('src', caseStudy.headerImage).attr('alt', caseStudy.title);
    
    // Update content
    $('#case-content').html(caseStudy.content);
}

